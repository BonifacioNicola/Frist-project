    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      
        
         Lavandaria lavandaria = (Lavandaria) this.tabela.getItem(this.jTable1.getSelectedRow());
         
          this.jT_Codigo.setText(String.valueOf(lavandaria.getCodigo()));
          this.jC_Tpeca.setSelectedItem(lavandaria.getTipo());
          this.jC_TLavagem.setSelectedItem(lavandaria.getLavandaria());
          this.jT_Cor.setText(lavandaria.getCor());
          this.Actualiuzar=this.Editar;
          
          this.jButton1.setEnabled(true);
          this.lavar=lavandaria;
          
    }//GEN-LAST:event_jTable1MouseClicked

