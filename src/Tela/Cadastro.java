    private void jB_GravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_GravarActionPerformed
        // TODO add your handling code here:
        if((!this.jT_Codigo.getText().equals(""))&&(!this.jT_Cor.getText().equals(""))){
           int resposta = JOptionPane.showConfirmDialog(null, "Gravar a Peca?", "Gravar", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if(resposta == 0){
        Lavandaria lavandaria=new Lavandaria(this.jC_Tpeca.getSelectedItem().toString(),this.jC_TLavagem.getSelectedItem().toString(),Integer.parseInt(this.jT_Codigo.getText()),this. jT_Cor.getText());
        
         if(this.Actualiuzar.equals(this.Gravar)){
             
        this.ficheiro.salvar(lavandaria);
        
         }else{
            lavandaria.setCodigo(this.lavar.getCodigo());
            this.ficheiro.actualizar(lavandaria); 
       } 
        this.array = ficheiro.listarTodos();
        this.tabela.setLinhas(array);
            
        this.limparCampos();
         this.jButton1.setEnabled(false);
        this.Actualiuzar = this.Gravar;
        
              }
        }else{
            JOptionPane.showMessageDialog(null,"Atencao! Preecha todos campos Para gravar","Alerta do erro",JOptionPane.INFORMATION_MESSAGE);
        }
            
    }//GEN-LAST:event_jB_GravarActionPerformed

